// drawLabel renders s at (x, y) in colour c using the mini font. Lowercases
// letters (the font only has lowercase) and skips unknown glyphs as blanks.
func drawLabel(img *image.RGBA, s string, x, y int, c color.RGBA) {
	cx := x
	for _, r := range s {
		if r >= 'A' && r <= 'Z' {
			r += 'a' - 'A'
		}
		g, ok := miniGlyphs[r]
		if ok {
			for row := 0; row < 7; row++ {
				bits := g[row]
				for col := 0; col < 5; col++ {
					if bits&(1<<uint(4-col)) != 0 {
						img.SetRGBA(cx+col, y+row, c)
					}
				}
			}
		}
		cx += 6 // 5px glyph + 1px gap
	}
}
