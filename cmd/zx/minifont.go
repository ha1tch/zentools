// A tiny self-contained 5x7 bitmap font for atlas labels, so the command stays
// dependency-free (no golang.org/x/image/font). Covers the characters that
// appear in asset labels: lowercase a-z, digits 0-9, space, and 'x' (used as the
// dimension separator, e.g. "16x16"). Unknown characters render as blank.

package main

import (
	"image"
	"image/color"
)

